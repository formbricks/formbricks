/** Languages whose script subtag is meaningful and must be preserved (Chinese Simplified/Traditional). */
const SCRIPT_SIGNIFICANT_LANGUAGES = new Set<string>(["zh"]);

/**
 * Resolve the canonical region-tagged BCP-47 tag for a code unknown to {@link LANGUAGE_CANONICAL_MAP}, using CLDR
 * likely-subtags. Returns null when the input can't be parsed into a language + region.
 */
function canonicalizeViaCldr(code: string): string | null {
  try {
    const locale = new Intl.Locale(code);
    const maximized = locale.maximize();
    const language = locale.language;
    if (!language) return null;
    const region = locale.region ?? maximized.region ?? null;
    if (!region) return null;
    const keepScript = Boolean(locale.script) || SCRIPT_SIGNIFICANT_LANGUAGES.has(language);
    const script = keepScript ? (locale.script ?? maximized.script ?? null) : null;
    return [language, script, region].filter(Boolean).join("-");
  } catch {
    return null;
  }
}

/**
 * Normalize any incoming language code — a legacy bare code, mixed case, `_` separators, a URL
 * `?lang=` value, or an SDK-persisted value — to its canonical region-tagged BCP-47 tag. Returns null when the
 * input can't be resolved. The static map is consulted first (deterministic and version-independent).
 * CLDR is the fallback for anything unrecognized.
 */
export function normalizeLanguageCode(input: string | null | undefined): string | null {
  if (!input) return null;
  const cleaned = input.trim().replace(/_/g, "-");
  if (cleaned.length === 0) return null;
  return LANGUAGE_CANONICAL_MAP[cleaned] ?? canonicalizeViaCldr(cleaned);
}
